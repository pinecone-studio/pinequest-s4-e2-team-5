// STT-ээс ирсэн монгол текстээс хүүхдийн сонгосон бодлогын дугаарыг салгана.
// Жишээ: "хоёр дахь бодлого" → 2, "бодлого гурвыг бодъё" → 3, "2-ыг сонгоё" → 2.
//
// requireKeyword=true үед зөвхөн сонголтын түлхүүр үг (бодлого, дугаар, дахь г.м.)
// агуулсан өгүүлбэрийг л дугаар гэж үзнэ — ингэснээр хүүхдийн энгийн хариу
// ("гурав") санамсаргүйгээр бодлого сонголт болж хувирахаас сэргийлнэ.

// Монгол тоо / эрэмбэт тооны үгс → бүхэл тоо.  1-10 хүртэл.
const NUM_WORDS = [
  [1, ['нэгдүгээр', 'нэгдэх', 'нэг дэх', 'нэг дахь', 'эхний', 'нэг']],
  [2, ['хоёрдугаар', 'хоёрдахь', 'хоёр дахь', 'хоёр дах', 'хоёрыг', 'хоёр', 'хойор']],
  [3, ['гуравдугаар', 'гурав дахь', 'гурвыг', 'гурван', 'гурав']],
  [4, ['дөрөвдүгээр', 'дөрөв дэх', 'дөрвөн', 'дөрөв', 'доров']],
  [5, ['тавдугаар', 'тав дахь', 'таван', 'тавыг', 'тав']],
  [6, ['зургаадугаар', 'зургаан', 'зургаа']],
  [7, ['долоодугаар', 'долоон', 'долоо']],
  [8, ['наймдугаар', 'найман', 'найм']],
  [9, ['есдүгээр', 'есөн', 'ес', 'юс']],
  [10, ['аравдугаар', 'арван', 'арав']],
]

const KEYWORD_RE = /бодлог|дугаар|дахь|дэх|дах|сонго|руу/

export function extractProblemNumber(rawText, { requireKeyword = false } = {}) {
  if (!rawText) return null

  const text = rawText
    .toLowerCase()
    .replace(/[.,!?;:"«»]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()

  if (requireKeyword && !KEYWORD_RE.test(text)) return null

  // Эхлээд цифрийг шалгана (ж: "2 дахь", "бодлого 3").
  const digit = text.match(/\b(\d{1,2})\b/)
  if (digit) {
    const n = parseInt(digit[1], 10)
    return n >= 1 ? n : null
  }

  // Дараа нь үгээр бичсэн тоог хайна.
  for (const [val, words] of NUM_WORDS) {
    for (const w of words) {
      if (text.includes(w)) return val
    }
  }

  return null
}
